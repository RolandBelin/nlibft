char		*ft_strrchr(const char *str, char c)
{
	unsigned int	i;

	i = 0;
	while (str[i])
		++i;
	while (i && str[i] != c)
		--i;
	return ((str[i] == c) ? (char *)str + i : (void *)0);
}
